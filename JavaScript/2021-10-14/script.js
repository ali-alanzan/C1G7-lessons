// Math Object

console.log( Math.ceil(12.24) ); // 13
console.log( Math.ceil(-12.24) ); // -12


console.log( Math.floor(11.99) ); // 11
console.log( Math.floor(-11.1) ); // -12


console.log( Math.round(8.4) ); // 8
console.log( Math.round(8.5) ); // 9


var nums = [1,34,64,25,345,345, 0.2];

console.log( Math.min( 1,34,64,25,345,345 ) ); // 1
console.log( Math.min( ...nums ) ); // 0.2


